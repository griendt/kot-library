@section('card-detail')
    Typically, a <def :d="'sj'">Saw Jump</def> is meant to be a jump involving jumping towards a Saw that is at the edge of a block or platform, then jumping on the very edge in order to pass the Saw. Almost all of these jumps are <def :d="'pd'">Path Dependent</def>. That means that, unlike most other jumps such as corner jumps and max height jumps, the way you approach the jump makes a difference in whether the jump is actually possible.<hr>
    As a result, some random approach to a <def :d="'sj'">Saw Jump</def> will almost certainly fail, regardless of the frame you attempted to perform the <def :d="'lj'">Last Jump</def>. You will need to make a series of correctly placed jumps before you can successfully perform the <def :d="'lj'">Last Jump</def> over the Saw.<br>
    This series of jumps is called a <def :d="'sjp'">Saw Jump Path</def>.
@overwrite
